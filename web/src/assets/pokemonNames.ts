const POKEMON_NAME_ENTRIES: PokemonNameEntry[] = EN_POKEMON_NAMES.map((en, index) => {
  const aliases = AUTO_ALIASES_BY_EN[en];
  return {
    en,
    zh: ZH_POKEMON_NAMES[index] ?? en,
    ja: JA_POKEMON_NAMES[index] ?? en,
    ...(aliases?.length ? { aliases } : {}),
  };
}).concat(EXTRA_POKEMON_NAME_ENTRIES, EXTRA_POKEMON_NAME_ENTRY_OVERRIDES);

const POKEMON_NAME_LOOKUP = new Map<string, PokemonNameEntry>();

for (const entry of POKEMON_NAME_ENTRIES) {
  const keys = new Set<string>([
    normalizePokemonLookupKey(entry.en),
    normalizePokemonLookupKey(entry.zh),
    normalizePokemonLookupKey(entry.ja),
    ...(entry.aliases ?? []).map((alias) => normalizePokemonLookupKey(alias)),
  ]);

  for (const key of keys) {
    if (!key) continue;
    POKEMON_NAME_LOOKUP.set(key, entry);
  }
}

const MAX_POKEMON_KEY_PARTS = (() => {
  let max = 1;

  for (const entry of POKEMON_NAME_ENTRIES) {
    const keys = [
      normalizePokemonLookupKey(entry.en),
      normalizePokemonLookupKey(entry.zh),
      normalizePokemonLookupKey(entry.ja),
      ...(entry.aliases ?? []).map((alias) => normalizePokemonLookupKey(alias)),
    ];

    for (const key of keys) {
      const count = key.split("-").filter(Boolean).length;
      if (count > max) max = count;
    }
  }

  return max;
})();

const CARD_SUFFIX_ORDER = ["vstar", "vmax", "gx", "ex", "v"] as const;
type CardSuffix = (typeof CARD_SUFFIX_ORDER)[number];

const CARD_SUFFIX_LABEL: Record<CardSuffix, Record<PokemonLocale, string>> = {
  vstar: { en: "VSTAR", zh: "VSTAR", ja: "VSTAR" },
  vmax: { en: "VMAX", zh: "VMAX", ja: "VMAX" },
  gx: { en: "GX", zh: "GX", ja: "GX" },
  ex: { en: "ex", zh: "ex", ja: "ex" },
  v: { en: "V", zh: "V", ja: "V" },
};

function splitCardSuffix(key: string): { baseKey: string; suffix: CardSuffix | "" } {
  for (const suffix of CARD_SUFFIX_ORDER) {
    if (key.endsWith(`-${suffix}`)) {
      return {
        baseKey: key.slice(0, -(suffix.length + 1)),
        suffix,
      };
    }
  }

  return {
    baseKey: key,
    suffix: "",
  };
}

function candidateLookupKeys(key: string) {
  const set = new Set<string>();
  if (!key) return [];

  set.add(key);

  if (key.startsWith("mega-")) {
    set.add(`${key.slice(5)}-mega`);
    set.add(key.slice(5));
  }

  if (key.endsWith("-mega")) {
    set.add(`mega-${key.slice(0, -5)}`);
    set.add(key.slice(0, -5));
  }

  if (key.startsWith("alolan-")) {
    set.add(`${key.slice(7)}-alola`);
    set.add(key.slice(7));
  }

  if (key.endsWith("-alola")) {
    set.add(`alolan-${key.slice(0, -6)}`);
    set.add(key.slice(0, -6));
  }

  for (const suffix of ["-origin", "-school", "-dusk", "-midnight", "-ultra", "-dusk-mane", "-pom-pom"]) {
    if (key.endsWith(suffix)) {
      set.add(key.slice(0, -suffix.length));
    }
  }

  for (const suffix of ["-teal-mask", "-wellspring-mask", "-hearthflame-mask", "-cornerstone-mask"]) {
    if (key.endsWith(suffix)) {
      set.add(key.slice(0, -suffix.length));
    }
  }

  if (key.startsWith("ogerpon-")) {
    set.add("ogerpon");
  }

  return [...set].filter(Boolean);
}

function findPokemonEntry(key: string): PokemonNameEntry | null {
  for (const candidate of candidateLookupKeys(key)) {
    const hit = POKEMON_NAME_LOOKUP.get(candidate);
    if (hit) return hit;
  }
  return null;
}

function findExactPokemonEntry(key: string): PokemonNameEntry | null {
  return POKEMON_NAME_LOOKUP.get(key) ?? null;
}

function humanizeToken(token: string) {
  const lower = token.toLowerCase();

  if (lower === "mega") return "Mega";
  if (lower === "alola" || lower === "alolan") return "Alolan";
  if (lower === "origin") return "Origin";
  if (lower === "school") return "School";
  if (lower === "dusk") return "Dusk";
  if (lower === "midnight") return "Midnight";
  if (lower === "ultra") return "Ultra";
  if (lower === "ex") return "ex";
  if (lower === "gx") return "GX";
  if (lower === "v") return "V";
  if (lower === "vmax") return "VMAX";
  if (lower === "vstar") return "VSTAR";
  if (lower === "x" || lower === "y" || lower === "z") return lower.toUpperCase();

  return token.charAt(0).toUpperCase() + token.slice(1);
}

function humanizePokemonKey(key: string) {
  return key
    .split("-")
    .filter(Boolean)
    .map(humanizeToken)
    .join(" ");
}

function tokenizeDeckName(raw: string) {
  return slugify(String(raw ?? "").replace(/[\\/_]+/g, " "))
    .split("-")
    .filter(Boolean);
}

function isCardSuffixToken(token: string): token is CardSuffix {
  return CARD_SUFFIX_ORDER.includes(token as CardSuffix);
}

function parseLocalizedDeckParts(rawName: string, locale: PokemonLocale) {
  const tokens = tokenizeDeckName(rawName);
  if (!tokens.length) return [];

  const parts: string[] = [];
  let index = 0;

  while (index < tokens.length) {
    let matchedEntry: PokemonNameEntry | null = null;
    let matchedLength = 0;
    const maxLength = Math.min(MAX_POKEMON_KEY_PARTS, tokens.length - index);

    for (let length = maxLength; length >= 1; length -= 1) {
      const candidate = tokens.slice(index, index + length).join("-");
      const entry = findExactPokemonEntry(candidate);
      if (!entry) continue;
      matchedEntry = entry;
      matchedLength = length;
      break;
    }

    if (!matchedEntry || matchedLength === 0) return [];

    let label = matchedEntry[locale];
    const nextToken = tokens[index + matchedLength];
    if (nextToken && isCardSuffixToken(nextToken)) {
      label = `${label} ${CARD_SUFFIX_LABEL[nextToken][locale]}`;
      matchedLength += 1;
    }

    parts.push(label);
    index += matchedLength;
  }

  return parts;
}

export function getLocalizedPokemonName(raw: string | undefined, locale: PokemonLocale) {
  const normalized = normalizePokemonLookupKey(raw ?? "");
  if (!normalized) return "";

  const exact = findPokemonEntry(normalized);
  if (exact) {
    return exact[locale];
  }

  const { baseKey, suffix } = splitCardSuffix(normalized);
  const baseEntry = findPokemonEntry(baseKey);
  const suffixLabel = suffix ? CARD_SUFFIX_LABEL[suffix][locale] : "";

  if (baseEntry) {
    const baseName = baseEntry[locale];
    return suffixLabel ? `${baseName} ${suffixLabel}` : baseName;
  }

  const fallbackBase = humanizePokemonKey(baseKey || normalized);
  return suffixLabel ? `${fallbackBase} ${suffixLabel}` : fallbackBase;
}

export function getLocalizedDeckNameFromIconKeys(
  iconKeys: string[],
  locale: PokemonLocale,
) {
  const names = iconKeys
    .slice(0, 2)
    .map((key) => getLocalizedPokemonName(key, locale))
    .filter(Boolean);

  const deduped = names.filter((name, index) => name !== names[index - 1]);

  if (deduped.length === 0) return "";
  return locale === "en" ? deduped.join(" ") : deduped.join(" / ");
}

export function getLocalizedDeckName(
  rawName: string | undefined,
  iconKeys: string[],
  locale: PokemonLocale,
) {
  const trimmed = String(rawName ?? "").trim();
  if (trimmed) {
    const parts = parseLocalizedDeckParts(trimmed, locale);
    if (parts.length > 0) {
      return locale === "en" ? parts.join(" ") : parts.join(" / ");
    }
  }

  return getLocalizedDeckNameFromIconKeys(iconKeys, locale);
}
